package com.kuznetsova.healthycafe.entity;

public class MenuType {
    private long id;
    private String name;

    public MenuType(String name) {
        this.name = name;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
